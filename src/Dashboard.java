    private void btnImageUploadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnImageUploadActionPerformed
        // TODO add your handling code here:
        //JFileChooser is Fiole Dialog of Swing!
        JFileChooser chooser = new JFileChooser();
        FileNameExtensionFilter imageFilter = new FileNameExtensionFilter("image files", ImageIO.getReaderFileSuffixes());
        
        chooser.setFileFilter(imageFilter);
        //Creates Open File Dialog
        chooser.showOpenDialog(this);
        //Returns the file which has been selected
        File f = chooser.getSelectedFile();
        //retrives the path of selected file
        if(f!=null){
            String  imagePath = f.getAbsolutePath();
            txtImagePath.setText(f.getAbsolutePath());
        
            try{
                BufferedImage image = Thumbnails.of(f).size(179,322).asBufferedImage();
                ImageIcon icon = new ImageIcon(image);
                lblImage.setIcon(icon);
                
                //Making My iMAGE to converted into byte[]
                ByteArrayOutputStream baos = new ByteArrayOutputStream();
                ImageIO.write(image,"jpeg", baos);
                byteImage = baos.toByteArray();
                
            }
            catch(Exception e){
                
            }
        }
        
    }//GEN-LAST:event_btnImageUploadActionPerformed

