    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        
        if(txtEmpID.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Please select some record to be deleted");
        }else{
            int btn = JOptionPane.showConfirmDialog(this,"Do you really want to delete","Delte", JOptionPane.YES_NO_OPTION);
            if(btn == JOptionPane.YES_OPTION){
                //you pressed yes
                try {
                    String sql = "Delete from employeeinfo where employee_id = ?";
                    ps = conn.prepareStatement(sql);
                    ps.setInt(1, Integer.parseInt(txtEmpID.getText()));
                    ps.execute();
                    sql = "Select * from employeeinfo";
                    updateTableData(sql);
                    clearFields();
                    btnUpdate.setEnabled(false);
                    btnDelete.setEnabled(false);
                    btnClear.setEnabled(false);
                    JOptionPane.showMessageDialog(this, "Record deleted successfully");
                } catch (Exception e) {
                    
                    JOptionPane.showMessageDialog(this, "Error while deleting");
                }
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

